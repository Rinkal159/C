#include <stdio.h>

int main()
{
    printf("Hello world!\n");

    // int
    int a = 4;
    printf("int is %d\n", a);

    // float
    float floatNum = 3.14;
    printf("float num is %f\n", floatNum);

    // double
    double doubleNum = 5.16789;
    printf("double jum is %lf\n", doubleNum);

    // char
    char chr = 'a';
    printf("char is %c\n", chr);

    // string
    char string[] = "Rinkal";
    printf("string is %s\n", string);
}